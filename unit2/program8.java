//Write a program that demonstrates that, no matter how many objects
//you create of a particular class, there is only one instance of a particular static field in that
//class. 


public class Main {
    static int a = 9;
    public static void main (String [] args){
        Main increment = new Main();
        Main decrement = new Main();
        System.out.println("Increment of " + increment.a + " is "+ ++increment.a);
        System.out.println("Decrement of " + decrement.a + " is "+ --decrement.a);
    }
}
